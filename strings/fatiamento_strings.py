nome = 'jose quintino de freitas junior'

print(nome[0]) # pega a letra da posição 0
print(nome[:5]) # pega do inicio ate a posição 5
print(nome[14:]) # pega da posição 8 até o fim
print(nome[5:14]) #pega da posição 5 ate a posição 14
print(nome[8:14:2]) # pega da posição 8 ate a posição 14 de 2 em 2
print(nome[:]) # pega do inicio ao final
print(nome[::-1]) # pega do inicio ao final invertendo